package team3.swS.purchs.vo;

public class OrdersVO {
	
	private String orders_no;
	private String orders_date;
	private String orders_amount;
	private String orders_mem_no;
	private String orders_voucher_name;
	
	public String getOrders_no() {
		return orders_no;
	}
	public void setOrders_no(String orders_no) {
		this.orders_no = orders_no;
	}
	public String getOrders_date() {
		return orders_date;
	}
	public void setOrders_date(String orders_date) {
		this.orders_date = orders_date;
	}
	public String getOrders_amount() {
		return orders_amount;
	}
	public void setOrders_amount(String orders_amount) {
		this.orders_amount = orders_amount;
	}
	public String getOrders_mem_no() {
		return orders_mem_no;
	}
	public void setOrders_mem_no(String orders_mem_no) {
		this.orders_mem_no = orders_mem_no;
	}
	public String getOrders_voucher_name() {
		return orders_voucher_name;
	}
	public void setOrders_voucher_name(String orders_voucher_name) {
		this.orders_voucher_name = orders_voucher_name;
	}
	
	
}
